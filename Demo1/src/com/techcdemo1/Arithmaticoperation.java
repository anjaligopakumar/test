package com.techcdemo1;

public class Arithmaticoperation {
	
	public int addition(int num1,int num2)
	{
		int sum = num1+num2;
		return sum;
	}
	
	public int difference(int num1,int num2)
	{
		int diff=num2-num1;
		return diff;
	}
	
	public int multiplication(int num1,int num2)
	{
		int mul=num1*num2;
		return mul;
	}

}
